package com.delphi.delphi.utils.git;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;

@JsonIgnoreProperties(ignoreUnknown = true)
public class GithubDirectory implements Entry {
    String type;
    String name;
    String path;
    String sha;

    public GithubDirectory(String type, String name, String path, String sha) {
        this.type = type;
        this.name = name;
        this.path = path;
        this.sha = sha;
    }

    @Override
    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    @Override
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getSha() {
        return sha;
    }

    public void setSha(String sha) {
        this.sha = sha;
    }
        
    @Override
    public String toString() {
        return String.format("""
                type: {type}
                name: {name}
                path: {path}
                sha: {sha}
                """, type, name, path, sha);
    }
}
